/**
 * Oracle Test Generator
 *
 * Auto-generates regression test cases from discrepancies between our
 * local parser and Onshape's compiler. Each false negative becomes a
 * test that our parser should eventually catch. Each false positive
 * becomes a test verifying our parser shouldn't flag it.
 */

import { writeFileSync, mkdirSync, readFileSync, existsSync } from 'node:fs';
import { resolve, dirname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = resolve(__dirname, '..');
const GENERATED_DIR = resolve(PROJECT_ROOT, 'test', 'oracle-generated');

/**
 * @typedef {Object} GeneratedTest
 * @property {string} name         - Test name
 * @property {string} sourceFile   - Original .fs file path
 * @property {string} snippet      - Extracted source lines around the error
 * @property {'false-negative'|'false-positive'} type
 * @property {number} line         - Line number of the diagnostic
 * @property {string} message      - Diagnostic message
 * @property {string} origin       - Timestamp tag
 */

/**
 * Generate test case data from a diff result.
 *
 * @param {import('./differ.js').DiffResult} diff
 * @param {string} source     - Full FeatureScript source
 * @param {string} sourceFile - Path to the .fs file
 * @returns {GeneratedTest[]}
 */
export function generateTests(diff, source, sourceFile) {
  const origin = `oracle-${new Date().toISOString().slice(0, 10)}`;
  const lines = source.split('\n');
  /** @type {GeneratedTest[]} */
  const tests = [];

  // False negatives → tests that our parser should eventually detect
  for (const fn of diff.falseNegatives) {
    tests.push({
      name: `[FN] ${basename(sourceFile)}:${fn.line} — ${sanitizeName(fn.message)}`,
      sourceFile,
      snippet: extractSnippet(lines, fn.line, 3),
      type: 'false-negative',
      line: fn.line,
      message: fn.message,
      origin,
    });
  }

  // False positives → tests verifying we shouldn't flag this
  for (const fp of diff.falsePositives) {
    tests.push({
      name: `[FP] ${basename(sourceFile)}:${fp.line} — ${sanitizeName(fp.message)}`,
      sourceFile,
      snippet: extractSnippet(lines, fp.line, 3),
      type: 'false-positive',
      line: fp.line,
      message: fp.message,
      origin,
    });
  }

  return tests;
}

/**
 * Write generated tests to the oracle-generated test directory.
 * Appends to an existing fixture file or creates a new one.
 *
 * @param {GeneratedTest[]} tests
 * @param {string} [outputDir]
 * @returns {{ path: string, count: number }}
 */
export function writeTestFixtures(tests, outputDir = GENERATED_DIR) {
  if (tests.length === 0) return { path: outputDir, count: 0 };

  mkdirSync(outputDir, { recursive: true });

  const fixturePath = resolve(outputDir, 'fixtures.json');
  const existing = loadExistingFixtures(fixturePath);

  // Deduplicate by name
  const existingNames = new Set(existing.map(t => t.name));
  const newTests = tests.filter(t => !existingNames.has(t.name));
  const merged = [...existing, ...newTests];

  writeFileSync(fixturePath, JSON.stringify(merged, null, 2) + '\n');

  // Also generate a runnable test file
  generateTestRunner(outputDir, merged);

  return { path: fixturePath, count: newTests.length };
}

/**
 * Generate a node:test runner that validates the fixtures.
 *
 * @param {string} outputDir
 * @param {GeneratedTest[]} fixtures
 */
function generateTestRunner(outputDir, fixtures) {
  const runnerPath = resolve(outputDir, 'oracle.test.js');

  const code = `// Auto-generated by oracle/test-generator.js — do not edit manually
import { describe, it } from 'node:test';
import { strict as assert } from 'node:assert';
import { readFileSync } from 'node:fs';
import { parse } from '../../parser/src/index.js';
import { lint } from '../../linter/src/index.js';

const fixtures = JSON.parse(readFileSync(new URL('./fixtures.json', import.meta.url), 'utf-8'));

describe('Oracle-generated regression tests', () => {
  for (const fixture of fixtures) {
    if (fixture.type === 'false-negative') {
      it(\`should detect: \${fixture.name}\`, { todo: true }, () => {
        // Our parser currently misses this — Onshape catches it.
        // When we improve the parser, remove the {todo} flag.
        const source = readFileSync(fixture.sourceFile, 'utf-8');
        const { errors } = parse(source);
        const { lintReports } = lint(source, fixture.sourceFile);
        const allDiags = [
          ...errors.map(e => ({ line: e.line ?? e.loc?.start?.line, message: e.message })),
          ...lintReports.map(r => ({ line: r.line, message: r.message })),
        ];

        const found = allDiags.some(d =>
          Math.abs(d.line - fixture.line) <= 3
        );

        assert.ok(found, \`Expected diagnostic near line \${fixture.line}: \${fixture.message}\`);
      });
    }

    if (fixture.type === 'false-positive') {
      it(\`should NOT flag: \${fixture.name}\`, () => {
        // Our parser incorrectly flags this — Onshape says it's fine.
        const source = readFileSync(fixture.sourceFile, 'utf-8');
        const { errors } = parse(source);
        const { lintReports } = lint(source, fixture.sourceFile);
        const allDiags = [
          ...errors.map(e => ({ line: e.line ?? e.loc?.start?.line, message: e.message })),
          ...lintReports.map(r => ({ line: r.line, message: r.message })),
        ];

        const falseHits = allDiags.filter(d =>
          Math.abs(d.line - fixture.line) <= 1
        );

        assert.strictEqual(falseHits.length, 0,
          \`Parser should not flag line \${fixture.line}, but found: \${JSON.stringify(falseHits)}\`
        );
      });
    }
  }
});
`;

  writeFileSync(runnerPath, code);
}

/**
 * @param {string} fixturePath
 * @returns {GeneratedTest[]}
 */
function loadExistingFixtures(fixturePath) {
  if (!existsSync(fixturePath)) return [];
  try {
    return JSON.parse(readFileSync(fixturePath, 'utf-8'));
  } catch {
    return [];
  }
}

/**
 * Extract lines around a target line number.
 * @param {string[]} lines
 * @param {number} targetLine - 1-indexed
 * @param {number} context    - Lines of context above/below
 * @returns {string}
 */
function extractSnippet(lines, targetLine, context) {
  const start = Math.max(0, targetLine - 1 - context);
  const end = Math.min(lines.length, targetLine + context);
  return lines.slice(start, end).join('\n');
}

/**
 * Sanitize a diagnostic message for use as a test name.
 * @param {string} message
 * @returns {string}
 */
function sanitizeName(message) {
  return message
    .replace(/[^a-zA-Z0-9 _\-().]/g, '')
    .slice(0, 80)
    .trim();
}
